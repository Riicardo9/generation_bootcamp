
public class TipoDeDatos {
	
	public static void main (String[] args) {
		
		//Tipos de datos primitivos
		
		//Tipos de datos numerico entero
		byte numMasPequenio = 100;
		short numPequenio = 32000;
		int numero = 54;
		long numGrande = 10000000;
		
		//numerico decimal
		float numDecimal = 1234.987f;
		double numDecimalGde = 2345678909876.8765;
		
		//booleanos
		boolean verdadero = true;
		boolean falso = false;
		
		//Caracter
		char letra = 'a'; //solo una letra, solo con comillas simples
		
		
		//Tipos de datos estructurados
		String nombre = "Ricardo";
		
		System.out.println(numMasPequenio);
		System.out.println(numPequenio);
		System.out.println(numero);
		System.out.println(numGrande);
		System.out.println(numDecimal);
		System.out.println(numDecimalGde);
		System.out.println(verdadero);
		System.out.println(falso);
		System.out.println(letra);
		System.out.println(nombre);
		
		
	}

}
